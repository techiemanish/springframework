public class Student{
    private int id;
    private String name;
    private String college;
    
    public void setId(int id){
        this.id = id;
    }
    public int getId(){
        return id;
    }
    
    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return name;
    }
    
    public void setCollege(String college){
        this.college = college;
    }
    public String getCollege(){
        return college;
    }
}